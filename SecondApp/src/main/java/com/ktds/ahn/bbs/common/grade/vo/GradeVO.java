package com.ktds.ahn.bbs.common.grade.vo;

public class GradeVO {

	private String gradeIndex;
	private String gradeName;
	private String conditionPoint;
	
	public String getGradeIndex() {
		return gradeIndex;
	}
	public void setGradeIndex(String gradeIndex) {
		this.gradeIndex = gradeIndex;
	}
	public String getGradeName() {
		return gradeName;
	}
	public void setGradeName(String gradeName) {
		this.gradeName = gradeName;
	}
	public String getConditionPoint() {
		return conditionPoint;
	}
	public void setConditionPoint(String conditionPoint) {
		this.conditionPoint = conditionPoint;
	}
}
